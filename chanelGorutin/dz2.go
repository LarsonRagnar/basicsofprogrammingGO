// Задание 2. Graceful shutdown
// Цель задания:
// Научиться правильно останавливать приложения.
// Что нужно сделать:
// -В работе часто возникает потребность правильно останавливать приложения.
// Например, когда наш сервер обслуживает соединения, а нам хочется,
// чтобы все текущие соединения были обработаны и лишь потом произошло выключение сервиса.
// Для этого существует паттерн graceful shutdown.
// -Напишите приложение, которое выводит квадраты натуральных чисел на экран,
// а после получения сигнала ^С обрабатывает этот сигнал, пишет «выхожу из программы» и выходит.
// Советы и рекомендации:
// Для реализации данного паттерна воспользуйтесь каналами и оператором select с default-кейсом.
package main

import (
	"fmt"
	"os"
	"os/signal"
	"syscall"
	"time"
)

func main() {
	// Канал для сигналов завершения
	shutdown := make(chan os.Signal, 1)

	// Регистрируем интересующие сигналы
	signal.Notify(shutdown, syscall.SIGINT, syscall.SIGTERM)

	// Канал для остановки генератора чисел
	stopChan := make(chan struct{})

	// Канал для чисел
	numbers := make(chan int)

	fmt.Println("Программа запущена. Нажмите Ctrl+C для завершения.")
	fmt.Println("Вывод квадратов натуральных чисел:")

	// Запускаем генератор чисел в отдельной горутине
	go func() {
		num := 1
		for {
			select {
			case <-stopChan:
				// Получен сигнал остановки
				fmt.Println("Генератор остановлен")
				close(numbers)
				return
			default:
				// Генерируем следующее число
				numbers <- num
				num++
				time.Sleep(100 * time.Millisecond)
			}
		}
	}()

	// Запускаем обработку в основной горутине
	// с использованием select для обработки сигналов
	for {
		select {
		case <-shutdown:
			// Получен сигнал завершения (Ctrl+C)
			fmt.Println("\nПолучен сигнал завершения. Завершаем работу...")

			// Останавливаем генератор
			close(stopChan)

			// Обрабатываем оставшиеся числа (если есть)
			fmt.Println("Обрабатываем оставшиеся числа...")
			processRemainingNumbers(numbers)

			fmt.Println("Выхожу из программы.")
			return

		case num, ok := <-numbers:
			if !ok {
				// Канал закрыт, выходим
				return
			}
			// Вычисляем и выводим квадрат
			square := num * num
			fmt.Printf("Число: %d, Квадрат: %d\n", num, square)
		}
	}
}

// Обработка оставшихся чисел в канале
func processRemainingNumbers(numbers <-chan int) {
	for num := range numbers {
		square := num * num
		fmt.Printf("[Завершение] Число: %d, Квадрат: %d\n", num, square)
	}
}
